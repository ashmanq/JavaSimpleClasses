public class Calculator {

    public int addTwoInts(int num1, int num2){
        return num1 + num2;
    }

    public int subtractTwoInts(int num1, int num2){
        return num1 - num2;
    }

    public int multiplyTwoInts(int num1, int num2){
        return num1 * num2;
    }

    public double divideTwoDoubles(double num1, double num2){
        return num1 / num2;
    }
}
