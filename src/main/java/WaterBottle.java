public class WaterBottle {

    private int volume;

    public void WaterBottle(){
        this.volume = 100;
    }

    public int getVolume(){
        return this.volume;
    }

    public int drinkFromWaterBottle(){
        return this.volume;
    }

    public int fillWaterBottle(){
        return this.volume;
    }

    public int emptyWaterBottle(){
        return this.volume;
    }
}
